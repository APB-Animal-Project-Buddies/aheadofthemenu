#!/usr/bin/env node
/**
 * Generates a local env file by PULLING variables from Vercel (decrypted).
 * The inverse of sync-vercel-env.mjs. Keeps secrets out of git: instead of a
 * committed template, a developer runs this to materialize .env.local.
 *
 * Usage:
 *   bun run env:pull                    # .env.local  <- Vercel "development"
 *   node scripts/pull-vercel-env.mjs <env-file> <scope-key>
 *
 * Requires (same as sync-vercel-env.mjs):
 *   - .vercel-config.json (committed, no secrets): { projectId, teamId, scopes }
 *   - a Vercel token via VERCEL_TOKEN or .vercel-config.local.json (gitignored)
 *     generate at https://vercel.com/account/tokens
 *
 * The output env file is gitignored (.env* is ignored) — do NOT commit it.
 */
import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const CONFIG_PATH = resolve(__dirname, "..", ".vercel-config.json");
const CONFIG_LOCAL_PATH = resolve(__dirname, "..", ".vercel-config.local.json");

const envFile = process.argv[2] ?? ".env.local";
const scopeKey = process.argv[3] ?? "dev";

if (!existsSync(CONFIG_PATH)) {
  console.error(`error: ${CONFIG_PATH} not found (needs projectId/teamId/scopes)`);
  process.exit(2);
}
const config = JSON.parse(readFileSync(CONFIG_PATH, "utf8"));

let vercelToken = process.env.VERCEL_TOKEN;
if (!vercelToken && existsSync(CONFIG_LOCAL_PATH)) {
  vercelToken = JSON.parse(readFileSync(CONFIG_LOCAL_PATH, "utf8")).vercelToken;
}
if (!vercelToken) {
  console.error("error: no Vercel token. Set VERCEL_TOKEN or add .vercel-config.local.json:");
  console.error("  { \"vercelToken\": \"<token from https://vercel.com/account/tokens>\" }");
  process.exit(2);
}

const projectId = process.env.VERCEL_PROJECT_ID ?? config.projectId;
const teamId = process.env.VERCEL_TEAM_ID ?? config.teamId;
if (!projectId || String(projectId).includes("REPLACE")) {
  console.error("error: set a real projectId in .vercel-config.json (or VERCEL_PROJECT_ID)");
  process.exit(2);
}

const scope = config.scopes?.[scopeKey];
const target = scope?.target?.[0];
if (!target) {
  console.error(`error: scopes["${scopeKey}"].target missing in .vercel-config.json`);
  console.error(`  available: ${Object.keys(config.scopes ?? {}).join(", ") || "<none>"}`);
  process.exit(2);
}

const teamQ = teamId ? `&teamId=${teamId}` : "";
const url = `https://api.vercel.com/v9/projects/${projectId}/env?decrypt=true${teamQ}`;

const res = await fetch(url, { headers: { Authorization: `Bearer ${vercelToken}` } });
const text = await res.text();
if (!res.ok) {
  console.error(`Vercel API ${res.status}: ${text}`);
  process.exit(1);
}
const { envs = [] } = JSON.parse(text);

// Keep vars scoped to the requested target (development/preview/production).
const wanted = envs.filter((e) => Array.isArray(e.target) && e.target.includes(target));

// Quote values that contain whitespace or shell-special chars so dotenv parses them intact.
const needsQuote = (v) => /[\s#'"$`\\]/.test(v);
const lines = [
  `# Generated by scripts/pull-vercel-env.mjs from Vercel (${target}). DO NOT COMMIT.`,
  ...wanted
    .sort((a, b) => a.key.localeCompare(b.key))
    .map(({ key, value }) => {
      const v = value ?? "";
      return needsQuote(v) ? `${key}="${v.replace(/"/g, '\\"')}"` : `${key}=${v}`;
    }),
  "",
];

writeFileSync(resolve(process.cwd(), envFile), lines.join("\n"));
console.log(`✓ wrote ${wanted.length} vars to ${envFile} (Vercel target=${target})`);
